UPDATE tour t
JOIN (
    SELECT 
        t.id AS tour_id,
        COALESCE(SUM(CASE WHEN l.location_type = 'HOTEL' THEN h.price END), 0) AS hotel_price_sum
    FROM tour t
    LEFT JOIN schedule s 
        ON s.tour_id = t.id
    LEFT JOIN location l 
        ON l.schedule_id = s.id
    LEFT JOIN hotel h 
        ON l.hotel_id = h.id
    GROUP BY t.id
) x ON t.id = x.tour_id
SET t.hotel_price_sum = x.hotel_price_sum;